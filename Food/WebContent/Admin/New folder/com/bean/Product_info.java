package com.bean;

public class Product_info {
	private int product_id,category_id;
	private double product_amount;
	private String product_name, product_description,product_image,product_size,product_availability;
	public int getProduct_id() {
		return product_id;
	}
	public void setProduct_id(int product_id) {
		this.product_id = product_id;
	}
	public int getCategory_id() {
		return category_id;
	}
	public void setCategory_id(int category_id) {
		this.category_id = category_id;
	}
	public double getProduct_amount() {
		return product_amount;
	}
	public void setProduct_amount(double product_amount) {
		this.product_amount = product_amount;
	}
	public String getProduct_name() {
		return product_name;
	}
	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}
	public String getProduct_description() {
		return product_description;
	}
	public void setProduct_description(String product_description) {
		this.product_description = product_description;
	}
	public String getProduct_image() {
		return product_image;
	}
	public void setProduct_image(String product_image) {
		this.product_image = product_image;
	}
	public String getProduct_size() {
		return product_size;
	}
	public void setProduct_size(String product_size) {
		this.product_size = product_size;
	}
	public String getProduct_availability() {
		return product_availability;
	}
	public void setProduct_availability(String product_availability) {
		this.product_availability = product_availability;
	}
	
	
}
